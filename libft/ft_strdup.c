#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t		i;
	char		*dup;

	if (!(dup = ft_strnew(ft_strlen(src))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
